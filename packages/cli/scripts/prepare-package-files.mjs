import { copyFileSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const PACKAGE_DIR = resolve(SCRIPT_DIR, "..");
const REPOSITORY_ROOT = resolve(PACKAGE_DIR, "../..");
const REPOSITORY_BLOB_URL = "https://github.com/wasabeef/AgentNote/blob/main";
const REPOSITORY_RAW_URL = "https://raw.githubusercontent.com/wasabeef/AgentNote/main";
const GENERATED_HEADER = "<!-- Generated from the repository README for npm packaging. Do not edit this file directly. -->\n\n";

function rewriteRepositoryLinks(markdown) {
  return markdown
    .replace(/href="\.\/(README\.[^"]+)"/g, `href="${REPOSITORY_BLOB_URL}/$1"`)
    .replace(/href="\.\/LICENSE"/g, `href="${REPOSITORY_BLOB_URL}/LICENSE"`)
    .replace(/href="(docs\/[^"]+)"/g, `href="${REPOSITORY_BLOB_URL}/$1"`)
    .replace(/src="(docs\/assets\/[^"]+)"/g, `src="${REPOSITORY_RAW_URL}/$1"`)
    .replace(/src="(website\/public\/images\/[^"]+)"/g, `src="${REPOSITORY_RAW_URL}/$1"`);
}

const rootReadme = readFileSync(join(REPOSITORY_ROOT, "README.md"), "utf-8");
const packageReadme = `${GENERATED_HEADER}${rewriteRepositoryLinks(rootReadme)}`;

writeFileSync(join(PACKAGE_DIR, "README.md"), packageReadme, "utf-8");
copyFileSync(join(REPOSITORY_ROOT, "LICENSE"), join(PACKAGE_DIR, "LICENSE"));
